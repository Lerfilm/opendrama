import React from "react"

interface IconProps {
  className?: string
  size?: number
}

function icon(d: string, opts?: { fill?: boolean; strokeWidth?: number }) {
  const component = ({ className, size = 24 }: IconProps) => (
    <svg
      xmlns="http://www.w3.org/2000/svg"
      width={size}
      height={size}
      viewBox="0 0 24 24"
      fill={opts?.fill ? "currentColor" : "none"}
      stroke="currentColor"
      strokeWidth={opts?.strokeWidth ?? 2}
      strokeLinecap="round"
      strokeLinejoin="round"
      className={className}
    >
      <path d={d} />
    </svg>
  )
  return component
}

function multiPath(paths: Array<{ d: string; fill?: boolean }>, opts?: { strokeWidth?: number }) {
  const component = ({ className, size = 24 }: IconProps) => (
    <svg
      xmlns="http://www.w3.org/2000/svg"
      width={size}
      height={size}
      viewBox="0 0 24 24"
      fill="none"
      stroke="currentColor"
      strokeWidth={opts?.strokeWidth ?? 2}
      strokeLinecap="round"
      strokeLinejoin="round"
      className={className}
    >
      {paths.map((p, i) => (
        <path key={i} d={p.d} fill={p.fill ? "currentColor" : "none"} />
      ))}
    </svg>
  )
  return component
}

